function deal(
  id: string,
  name: string,
  price: number,
  category: Product["category"],
  discount: number,
  spec = "Standard",
  idx = 0
): Product {
  const originalPrice = Math.round(price / (1 - discount / 100));
  return {
    id,
    name,
    description: name,
    price,
    originalPrice,
    discount,
    rating: 3.5 + (idx % 15) / 10,
    reviewCount: 80 + idx * 137,
    seller: idx % 3 === 0 ? "Section Eight Official" : "Verified Seller",
    color: "Black",
    spec,
    image: getProductPhotoUrl(id, category),
    category,
    availableColors: ["Black", "White"],
    isBudget: price < 100000,
    badge: discount >= 40 ? "Hot Deal" : discount >= 25 ? "Deal" : undefined,
  };
}

